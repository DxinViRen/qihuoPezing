#import "NSArray+Adpa.h"

@implementation NSArray (Adpa)
- (id)dx_objectAtIndex:(NSUInteger)index
{
    if (index >= [self count]) {
        return nil;
    }
    id value = [self objectAtIndex:index];
    if (value == [NSNull null]) {
        return nil;
    }
    return value;
}
@end
